  /**
   * Format code style patterns
   */
  private formatCodeStylePatterns(patterns: any): string {
    let content = '### Naming Conventions\n';
    for (const [key, value] of Object.entries(patterns.namingConventions || {})) {
      content += `- ${key}: ${value}\n`;
    }

    content += `\n### Package Structure\n${patterns.packageStructure}\n`;
    content += `\n### Code Style\n`;
    content += `- Indentation: ${patterns.indentation}\n`;
    content += `- Line Length: ${patterns.lineLength}\n`;
    content += `- Documentation Style: ${patterns.docstringStyle}\n`;

    return content;
  }

